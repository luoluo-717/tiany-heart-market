function 战斗主控:结束流程(内容)
	for i=1,#内容 do
		for k,v in pairs(内容[i]) do
			if 内容[i].挨打方 ~= nil and self.战斗单位[内容[i].挨打方] ~= nil then
				if k == "取消状态" then
					for s=1,#内容[i].取消状态 do
						self.战斗单位[内容[i].挨打方].状态特效[内容[i].取消状态[s]]=nil
						if qxbx[内容[i].取消状态[s]] then
							self.战斗单位[内容[i].挨打方]:取消变相()
							if 内容[i].取消状态[s] == "分身术" or 内容[i].取消状态[s] == "修罗隐身" then
								self.战斗单位[内容[i].挨打方]:取消隐身()
							end
						end
					end
				elseif k == "复活" then
					self.战斗单位[内容[i].挨打方].停止更新=false
					self.战斗单位[内容[i].挨打方]:换动作("待战")
					self.战斗单位[内容[i].挨打方]:设置掉血(内容[i].复活.气血,2)
					self.战斗单位[内容[i].挨打方]:换动作("待战")
				elseif k == "法术状态" then
					for z,x in pairs(内容[i].法术状态) do
						if self.战斗单位[内容[i].挨打方].状态特效[z] == nil then
							self.战斗单位[内容[i].挨打方]:增加状态(z)
							self.战斗单位[内容[i].挨打方].状态特效[z].回合 = 内容[i].法术状态[z].回合
						else
						    self.战斗单位[内容[i].挨打方].状态特效[z].回合 = 内容[i].法术状态[z].回合
						end
						if 内容[i].法术状态[z].层数~=nil then
						    self.战斗单位[内容[i].挨打方].状态特效[z].层数=内容[i].法术状态[z].层数
						end
					end
				elseif k == "人物状态" then
					self.战斗单位[内容[i].挨打方]:结束同步(内容[i].人物状态.气血,内容[i].人物状态.最大气血,内容[i].人物状态.气血上限,内容[i].人物状态.魔法,内容[i].人物状态.最大魔法,内容[i].人物状态.愤怒,内容[i].人物状态.护盾,内容[i].人物状态.战意,内容[i].人物状态.五行珠,内容[i].人物状态.人参果,内容[i].人物状态.骤雨,内容[i].人物状态.超级战意)
				elseif k == "结尾回血" then 
					self.战斗单位[内容[i].挨打方]:设置掉血(内容[i].结尾回血.气血,2)
				elseif k == "共生" then
					self.战斗单位[内容[i].挨打方].共生=true
				elseif k == '黎魂' then
					self.战斗单位[内容[i].挨打方].黎魂 = v	
				elseif k == "丸子附加" then
					self.战斗单位[内容[i].挨打方].超级偷袭 = v.超级偷袭
				end
			end
		end
	end
	for i=1,#self.特殊状态 do --这里加结束流程 特殊状态加在这？
		if self.特殊状态[i] ~= nil then
			if self.特殊状态[i].回合 == 0 then
				self:特殊状态处理(self.特殊状态[i].名称,2)
				self.特殊状态[i] = nil
				table.remove(self.特殊状态,i)
			else
				self.特殊状态[i].回合 = self.特殊状态[i].回合 -1
			end
		end
	end
end
