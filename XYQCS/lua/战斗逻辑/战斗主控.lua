			多开数据[self.当前].__主控.奇袭窗口光2:显示(0,引擎.高度)
			多开数据[self.当前].__主控.奇袭窗口光3:显示(引擎.宽度,0)
			多开数据[self.当前].__主控.奇袭窗口光4:显示(引擎.宽度,引擎.高度)

		end
	end
	-- self:显示攻击特效1(dt)

	if self.进程 == "命令" then
		self.秒显示 = 0
		self.分显示 = 0
		self.结果 = os.time() - self.命令数据.计时
		self.显示时间 = 0
		--print(self.结果)
		if self.结果 >= 1 then
			self.命令数据.计时 = os.time()
			self.命令数据.秒 = self.命令数据.秒 - 1
			if self.命令数据.秒 < 0 then
				if self.命令数据.分 <= 0 and self.命令数据.秒 <= 0 then
					多开数据[self.当前].__UI界面.界面层.战斗界面:置可见(false)
