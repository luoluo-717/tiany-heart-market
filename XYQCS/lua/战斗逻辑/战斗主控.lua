	if self.战斗流程[1].溅射 ~= nil and  self.战斗流程[1].溅射.完成== nil then
		for i=1,#self.战斗流程[1].溅射 do
			if self.战斗单位[self.战斗流程[1].溅射[i].挨打方] ~= nil then
				if not self.战斗流程[1].溅射[i].死亡 then
					self.战斗单位[self.战斗流程[1].溅射[i].挨打方]:换动作("挨打", nil, true)
                	self.战斗单位[self.战斗流程[1].溅射[i].挨打方].动画.停止更新 = true
