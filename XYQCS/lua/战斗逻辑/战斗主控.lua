			for n=1,#self.战斗流程[1].附加施法[1].挨打方 do
				if self.战斗流程[1].附加施法[1].挨打方[n].类型 == 2 then
					self.战斗单位[self.战斗流程[1].附加施法[1].挨打方[n].挨打方]:设置掉血(self.战斗流程[1].附加施法[1].挨打方[n].伤害,self.战斗流程[1].附加施法[1].挨打方[n].类型)
