	end
	if self.战斗流程[1].减少魔法 ~= nil and self.战斗流程[1].减少魔法.完成 == nil then
		for i=1,#self.战斗流程[1].减少魔法 do
			self.战斗单位[self.战斗流程[1].减少魔法[i].挨打方]:魔法更新(self.战斗流程[1].减少魔法[i].魔法)
		end
