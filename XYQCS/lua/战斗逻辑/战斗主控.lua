function 战斗主控:取攻击位置(编号, 目标, 不计算浮空高度)
    local 攻击方 = self.战斗单位[编号]
    local 挨打方 = self.战斗单位[目标]
    local 临时模型 = 攻击方.模型
    if 攻击方.变身数据 ~= nil then
        临时模型 = 攻击方.变身数据
    end
    local ox, oy = self:攻击坐标调整(临时模型, 攻击方.武器子类)
    local 攻击位置x, 攻击位置y
    if 挨打方.敌我 == 1 then  -- 挨打方是我方（左侧）
        攻击位置x = 挨打方.显示xy.x + ox
        攻击位置y = 挨打方.显示xy.y - oy
    else  -- 挨打方是敌方（右侧）
        攻击位置x = 挨打方.显示xy.x - ox
        攻击位置y = 挨打方.显示xy.y + oy
    end
    if 攻击方.超级偷袭 then
        local 模型 = 攻击方.模型
        local 武器子类 = 攻击方.武器子类
        local extraX, extraY = self:超级偷袭偏移量(模型, 武器子类)
        if 攻击方.敌我 == 1 then  -- 攻击方是我方
            攻击位置x = 攻击位置x - extraX
            攻击位置y = 攻击位置y - extraY
        else  -- 攻击方是敌方
            攻击位置x = 攻击位置x + extraX
            攻击位置y = 攻击位置y + extraY
        end
    end
    return 攻击位置x, 攻击位置y
end

