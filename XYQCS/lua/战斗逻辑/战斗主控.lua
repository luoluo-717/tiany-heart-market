		self.魔火焚世.动画:显示(self.魔火焚世.x, self.魔火焚世.y)
	end
	if self.媚眼如丝 ~= nil then
		self.媚眼如丝.动画:显示(__引擎宽高2(1), __引擎宽高2(2))
	end
	if self.清静菩提 ~= nil then
		self.清静菩提.动画:显示(__引擎宽高2(1), __引擎宽高2(2))
	end
	if self.单挑模式 and self.进程 == "命令" then
	多开数据[self.当前].__UI界面.界面层.战斗界面:单挑显示(x,y)

	end

	if self.背景状态 == 1 then
		self.黑幕背景:显示(0,0)
	end

	

	for i = 1, #self.显示表 do
		if self.显示表[i].特效 then
			self.显示表[i].特效:显示(math.floor(self.拼接偏移.x + self.显示表[i].偏移.x), math.floor(self.拼接偏移.y + self.显示表[i].偏移.y))
		else
			self.显示表[i]:显示(x, y)
		end
	end
	------------------------------------------------C改
	for i=1,#self.战斗单位 do
		if self.战斗单位[i] and self.战斗单位[i].显示xy then
			self.显示排序[i] = {显示y=self.战斗单位[i].显示xy.y,编号=i}
		end
	end
	table.sort(self.显示排序,排序1)
	for n=1,#self.显示排序 do
		if self.显示排序[n] and self.显示排序[n].编号 then
			local 单位编号 = self.显示排序[n].编号
			if self.进程 == "命令" and 
			多开数据[self.当前].__UI界面.界面层.战斗界面.单位编号 and
			多开数据[self.当前].__UI界面.界面层.战斗界面.操作对象 and
			多开数据[self.当前].__UI界面.界面层.战斗界面.单位编号[多开数据[self.当前].__UI界面.界面层.战斗界面.操作对象] == 单位编号 and 
			self.战斗单位[单位编号] and 
			not self.战斗单位[单位编号].数据.自动战斗 then
				
			多开数据[self.当前].__主控.战斗指向:更新(0.01)
			多开数据[self.当前].__主控.战斗指向:显示(
				math.floor(self.战斗单位[单位编号].显示xy.x-29),
				math.floor(self.战斗单位[单位编号].显示xy.y - self.战斗单位[单位编号].高度)
			)
			end
		end
	end
	------------------------------------------------C改
	if self.三按钮身光 then
		local 单位编号
		if 多开数据[self.当前].__UI界面.界面层.战斗界面 then
			单位编号 = 多开数据[self.当前].__UI界面.界面层.战斗界面.单位编号[多开数据[self.当前].__UI界面.界面层.战斗界面.操作对象]
		end
		
		if 单位编号 and self.战斗单位[单位编号] then
			多开数据[self.当前].__主控.奇袭身光:更新(0.02)
			多开数据[self.当前].__主控.奇袭身光:显示(
				math.floor(self.战斗单位[单位编号].显示xy.x ),
				math.floor(self.战斗单位[单位编号].显示xy.y)
			)

