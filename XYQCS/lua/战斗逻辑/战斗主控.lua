					self.显示时间 = 1
				elseif self.命令数据.秒 <= 0 then
					self.命令数据.秒 = 9
					self.命令数据.分 = self.命令数据.分 - 1
				end
			end
		end
		if self.显示时间 == 0 then
			self.分显示 = self.命令数据.分 + 1
