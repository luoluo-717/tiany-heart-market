function 战斗主控:显示(dt,x, y)
	if self.战斗类型==111125.1 then
	self.地煞背景:显示(0, 0)
	elseif self.战斗类型==111130 then
	self.天罡背景:显示(0, 0)
	else
	self.背景:显示(0, 0)
	end

	

	if self.无间地狱 == nil then
		if self.战斗类型~=111125.1 and self.战斗类型~=111130 then
		self.背景圆:显示(__引擎宽高2(1) - 275, __引擎宽高2(2) - 125)
		end
	else
		self.无间地狱:显示(__引擎宽高2(1)-60, __引擎宽高2(2)+20)
	end
