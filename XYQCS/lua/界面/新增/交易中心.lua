end
local 控件名称={"装备_控件","宠物_控件","杂货_控件"}
function 交易中心:更新数据(内容)
  -- table.print(内容)
  for i = 1, #控件名称 do
    if self[控件名称[i]] then
      self[控件名称[i]]:置可见(false)
    end
  end
  
  self.数据=内容
  self.名称={}
	for i=1,#self.数据 do
		local 临时名称=self.数据[i].名称
		if self.状态 == 3 then  -- 杂货类
			-- 直接使用物品名称，不需要拼接任何参数
		else
			-- 原有的处理逻辑
			if 临时名称 == "制造指南书" then
				临时名称=self.数据[i].参数一.."级"..多开数据[当前].__主控:取武器子类(self.数据[i].参数二)..临时名称
			elseif 临时名称 == "百炼精铁" then
				临时名称=self.数据[i].参数一.."级"..临时名称
			elseif 临时名称 == "魔兽要诀" or 临时名称 == "高级魔兽要诀" then
				临时名称=self.数据[i].参数一
			end
		end
		-- 假设数据中有数量字段，如果没有则需要从服务端添加
		local 数量信息 = self.数据[i].数量 or 0
		self.名称[#self.名称+1]={名称=临时名称,价格=self.数据[i].价格,日涨跌幅=self.数据[i].日涨跌幅,数量=数量信息}
	end
  if self[控件名称[self.状态]] then
    self[控件名称[self.状态]]:置可见(true)
    self[控件名称[self.状态]]:重置()
    if self.状态==1 then
      交易中心.装备相关:置选中(true)
    elseif self.状态==2 then
      交易中心.宠物相关:置选中(true)
    elseif self.状态==3 then
      交易中心.杂货相关:置选中(true)
    end
  end
end
function 交易中心:加载按钮()
  
  for i = 1, #控件名称 do
    if self.分类数据[i] and self[控件名称[i]] then
      for a = 1, #self.分类数据[i] do
        if not self[控件名称[i]]["按钮"..a] then
          local 临时函数 = self[控件名称[i]]["创建我的单选按钮"](self[控件名称[i]], __res:getPNGCC(3, 1041, 57, 132, 40, true):拉伸(100,35), __res:getPNGCC(3, 1041, 9, 132, 42, true):拉伸(100,35),"按钮"..a, 19+(a-1)*109, 36+5,self.分类数据[i][a])
          function  临时函数:左键弹起(x, y)
            交易中心.选中编号=nil
            交易中心.子类状态 = a
            发送数据(当前,901,{大类=交易中心.状态,小类=交易中心.分类数据[交易中心.状态][交易中心.子类状态]})
          end
          临时函数:置可见(true)
        end
      end
    end
  end
  交易中心.装备_控件:重置()
end




