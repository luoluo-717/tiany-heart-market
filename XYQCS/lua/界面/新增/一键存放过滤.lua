function 道具网格:置物品(过滤列表, 当前页码)
    -- 解析所有物品
    local 所有物品 = {}
    if type(过滤列表) == "string" and 过滤列表 ~= "" then
        for 物品名 in string.gmatch(过滤列表, "[^,]+") do
            物品名 = 物品名:gsub("^%s+", ""):gsub("%s+$", "")
            if 物品名 ~= "" then
                table.insert(所有物品, {名称 = 物品名})
            end
        end
    end
    
    -- 计算当前页显示的物品（分页逻辑）
    local 每页数量 = 6*3 -- 18个
    local 当前页物品 = {}
    local 起始索引 = (当前页码 - 1) * 每页数量 + 1
    local 结束索引 = math.min(当前页码 * 每页数量, #所有物品)
    for i = 起始索引, 结束索引 do
        table.insert(当前页物品, 所有物品[i])
    end
    
    -- 循环网格子控件，显示当前页物品
    for i = 1, #self.子控件 do
        if 当前页物品[i] then 
            local lssj = __物品格子.创建(当前 or self.当前)
            lssj:置物品(当前页物品[i], nil, "一键存放过滤")
            lssj:置偏移(0, 0) -- 偏移量适配新网格
            self.子控件[i]:置精灵(lssj)
        else
            self.子控件[i]:置精灵() -- 清空空白格子
        end
    end
end

function 道具网格:左键弹起(x, y, a, b, msg)
    if self.子控件[a] and self.子控件[a]._spr then
        self.子控件[a]._spr:详情打开(350,110)
    end
end

