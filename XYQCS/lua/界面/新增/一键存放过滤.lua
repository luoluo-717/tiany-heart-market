local 添加 = 一键存放过滤:创建我的按钮(__res:getPNGCC(1,401,65,175,43,true):拉伸(110,35),"添加",60,450,"添加" )
function 添加:左键弹起(x,y,msg)
    if 一键存放过滤.类型 ~= "一键存放过滤" then
        return
    end
    local 新输入文本 = 一键存放过滤.内容输入:取文本() or ""
    新输入文本 = string.gsub(新输入文本, "　", " ")
    if 新输入文本 == "" then
        多开数据[当前 or self.当前].__UI弹出.提示框:打开("#Y请输入要新增的道具名（多个用逗号分隔）")
        return
    end
    local 现有道具列表 = {}
    local 已存在标记 = {}  
    for 道具名 in string.gmatch(一键存放过滤.当前过滤列表, "[^,]+") do
        道具名 = string.gsub(道具名, "^%s+", ""):gsub("%s+$", "")  
        if 道具名 ~= "" then
            现有道具列表[#现有道具列表 + 1] = 道具名
            已存在标记[道具名] = true
        end
    end
    
    local 新道具列表 = {}
    local 非法道具 = {}  
    for 道具名 in string.gmatch(新输入文本, "[^,]+") do
        道具名 = string.gsub(道具名, "^%s+", ""):gsub("%s+$", "")  
        if 道具名 == "" then
            goto 跳过空项  
        end
        
        local 合法, 原因 = 判断文本合法性(道具名)
        if not 合法 then
            非法道具[#非法道具 + 1] = 道具名 .. "（" .. 原因 .. "）"
            goto 跳过空项
        end
        
        if not 已存在标记[道具名] then
            新道具列表[#新道具列表 + 1] = 道具名
            已存在标记[道具名] = true  
        end
        ::跳过空项::
    end
    
    if #非法道具 > 0 then
        多开数据[当前 or self.当前].__UI弹出.提示框:打开(
            "#R以下道具名不合法：\n" .. table.concat(非法道具, "\n")
        )
        return  
    end
    
    if #新道具列表 == 0 then
        多开数据[当前 or self.当前].__UI弹出.提示框:打开("#Y没有新增有效道具（已存在或为空）")
        一键存放过滤.内容输入:清空()
        return
    end
    
    local 合并后列表 = {}
    for _, v in ipairs(现有道具列表) do
        合并后列表[#合并后列表 + 1] = v
    end
    for _, v in ipairs(新道具列表) do
        合并后列表[#合并后列表 + 1] = v
    end
    
    local 新过滤文本 = table.concat(合并后列表, ",")
    if 多开数据[当前] then
        发送数据(当前, 8003.2, {过滤列表 = 新过滤文本})
        多开数据[当前].__UI弹出.提示框:打开("#G成功新增 " .. #新道具列表 .. " 个道具到过滤列表")
        -- 新增后刷新当前页
        一键存放过滤.当前过滤列表 = 新过滤文本
        一键存放过滤.道具网格:置物品(新过滤文本, 一键存放过滤.当前页码)
        一键存放过滤:置文字()
    else
        多开数据[self.当前 or 当前].__UI弹出.提示框:打开("#R数据异常，无法发送")
    end
    一键存放过滤.内容输入:清空()
end

