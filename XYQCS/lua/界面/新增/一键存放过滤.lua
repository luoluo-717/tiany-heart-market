-- 校验文本是否包含特殊字符/敏感词（复用现有逻辑，可通用校验道具名）
local function 判断文本合法性(mz)
    -- 过滤特殊字符（空格、标点、控制字符等）
    if string.find(mz,"[%s%p%c%z%?\\!@#%$%%&%*%(%)%^,%.%+%-/<>;'\"%[%]{}]") then
        return false, "包含特殊字符"
    -- 过滤全角空格、GM相关、敏感词
    elseif string.find(mz,"　") 
        or string.find(mz, "GM") 
        or string.find(mz, "Gm") 
        or string.find(mz, "gm") 
        or string.find(mz, "充值") 
        or string.find(mz, "管理") 
        or string.find(mz, "老猫") 
        or string.find(mz, "国家") 
        or string.find(mz, "主席") 
        or string.find(mz, "近平") then
        return false, "包含敏感内容"
    end
    return true
end

function 一键存放过滤:初始化()
    self.输入栏文本:置文本("#W请输入道具名（多个用逗号(,)分隔）")
    self.允许移动 = true
