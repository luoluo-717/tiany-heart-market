-- 辅助函数：解析过滤列表为物品数组
function 一键存放过滤:解析过滤列表(过滤列表)
    local 物品列表 = {}
    if type(过滤列表) == "string" and 过滤列表 ~= "" then
        for 物品名 in string.gmatch(过滤列表, "[^,]+") do
            物品名 = 物品名:gsub("^%s+", ""):gsub("%s+$", "")
            if 物品名 ~= "" then
                table.insert(物品列表, {名称 = 物品名})
            end
        end
    end
    return 物品列表
end

