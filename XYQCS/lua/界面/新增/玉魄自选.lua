function 确定:左键弹起(消息,x,y)
    -- 验证必选属性
    if not 玉魄自选.基础属性 then
        多开数据[当前].__UI弹出.提示框:打开("请选择玉魄基础属性")
        return
    end
    
    if not 玉魄自选.附加属性[1] or not 玉魄自选.附加属性[2] then
        多开数据[当前].__UI弹出.提示框:打开("请选择2个玉魄附加属性")
        return
    end
    
    if not 玉魄自选.额外属性 then
        多开数据[当前].__UI弹出.提示框:打开("请选择玉魄额外属性")
        return
    end
    
    if not 玉魄自选.特殊属性 then
        多开数据[当前].__UI弹出.提示框:打开("请选择玉魄特殊属性")
        return
    end
    
    local 事件 = function()
        local 发送信息 = {
            基础 = {类型 = 玉魄自选.基础属性},
            附加 = {},
            额外 = {类型 = 玉魄自选.额外属性},
            特殊 = {类型 = 玉魄自选.特殊属性}
        }
        
        for i=1,2 do
            if 玉魄自选.附加属性[i] then
                发送信息.附加[i] = {类型 = 玉魄自选.附加属性[i]}
            end
        end
        
        发送数据(当前 or self and self.当前,3757,{幻化属性=发送信息})
        
        -- 清空选择
        玉魄自选.基础属性 = nil
        玉魄自选.基础数值 = nil
        玉魄自选.附加属性 = {}
        玉魄自选.附加数值 = {}
        玉魄自选.额外属性 = nil
        玉魄自选.额外数值 = nil
        玉魄自选.特殊属性 = nil
        玉魄自选.特殊数值 = nil
        玉魄自选:置可见(false)
    end
    
    local wb = "属性一旦选定无法更改,你确定要提交么?"   
    多开数据[当前].__UI界面.窗口层.文本栏:打开(wb,285,155,390,200,事件)
end

