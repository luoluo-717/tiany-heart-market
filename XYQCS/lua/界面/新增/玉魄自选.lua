    local dj = 取物品(item_mc)
    self:置图像(dj,item_mc)
    for i=1,2 do
        if 玉魄自选["加"..i] then
            玉魄自选["加"..i]:置可见(true)
        end
    end
    if 玉魄自选.额外属性按钮 then
        玉魄自选.额外属性按钮:置可见(true)
