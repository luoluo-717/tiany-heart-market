local 阵法系统格子 = class("阵法系统格子")
function 阵法系统格子:初始化()
end
local 按钮资源 = {
    正常 = {3, 880, 331, 86, 37},
    选中 = {3, 876, 289, 85, 36}

}

function 阵法系统格子:置数据(name)  -- name为阵法名称（如"天覆阵"）
    -- 创建正常状态图像（含名字）
    local nsf = require("SDL.图像")(130, 80)
    if nsf["渲染开始"](nsf) then
        -- 绘制按钮背景（复用升级按钮样式）
        __res:getPNGCC(
            按钮资源.正常[1], 
            按钮资源.正常[2], 
            按钮资源.正常[3], 
            按钮资源.正常[4], 
            按钮资源.正常[5], 
            true
        ):拉伸(80, 30):显示(0, 25)  -- 按钮背景居中
        字体16:置颜色(__取颜色("白色"))  -- 文字颜色（与按钮适配）
        local 文字图像 = 字体16["取图像"](字体16, name)  -- 生成文字图像
        -- 计算文字位置：按钮背景居中（5+70/2 - 文字宽度/2，25+30/2 - 文字高度/2）
        local 文字X = 5 + (70 - 文字图像["宽度"]) // 2
        local 文字Y = 25 + (30 - 文字图像["高度"]) // 2
        文字图像:显示(文字X, 文字Y)  -- 绘制文字
        
        nsf["渲染结束"](nsf)
    end
    self.图像 = nsf["到精灵"](nsf)
    nsf = require("SDL.图像")(130, 80)
    if nsf["渲染开始"](nsf) then
        __res:getPNGCC(
            按钮资源.选中[1], 
            按钮资源.选中[2], 
            按钮资源.选中[3], 
            按钮资源.选中[4], 
            按钮资源.选中[5], 
            true
        ):拉伸(80, 30):显示(0, 25)
        
        -- 绘制阵法名称（与正常状态位置、样式一致）
        字体16:置颜色(__取颜色("白色"))
        local 文字图像 = 字体16["取图像"](字体16, name)
        local 文字X = 5 + (70 - 文字图像["宽度"]) // 2
        local 文字Y = 25 + (30 - 文字图像["高度"]) // 2
        文字图像:显示(文字X, 文字Y)
        
        nsf["渲染结束"](nsf)
    end
    self.图像2 = nsf["到精灵"](nsf)
end

-- 保持原有显示逻辑
function 阵法系统格子:显示(x, y)
    if self.图像 and not self.确定 then
        self.图像["显示"](self.图像, x, y)
    end
    if self.图像2 and self.确定 then
        self.图像2["显示"](self.图像2, x, y)
    end
end

return 阵法系统格子