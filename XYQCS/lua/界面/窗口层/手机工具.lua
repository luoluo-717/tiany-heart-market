function 手机工具:重刷()
  for i=1,#xsan do
    if xsan[i] and self[xsan[i].."钮"] then
      self[xsan[i].."钮"]:置选中(self.类型==i)
    end
  end
  
  -- 隐藏和禁用所有类型的控件
  self:隐藏和禁用所有控件()
  
  -- 显示和启用当前类型的控件
  if self.类型 == 1 then
    self:显示充值系统控件()
  elseif self.类型 == 2 then
    self:显示角色系统控件()
  elseif self.类型 == 3 then
    self:显示物品系统控件()
  elseif self.类型 == 4 then
    self:显示装备系统控件()
  elseif self.类型 == 5 then
    self:显示宠物系统控件()
  -- elseif self.类型 == 6 then
  --   self:显示其他系统控件()
  -- elseif self.类型 == 7 then
  --   self:显示活动设置控件()
  end
    -- 其他系统控件
  local xlx = 6
  if xsjl[xlx] then
    for i=1,#xsjl[xlx].项目 do
      if xsjl[xlx].项目[i] and self[xsjl[xlx].项目[i]..'输'..xlx] then
        self[xsjl[6].项目[i]..'输'..xlx]:置可见(self.类型==xlx)
        self[xsjl[6].项目[i]..'输'..xlx..'-0']:置可见(self.类型==xlx)
        self[xsjl[6].项目[i]..'选'..xlx]:置可见(self.类型==xlx)
        self[xsjl[6].项目[i]..'改'..xlx]:置可见(self.类型==xlx)
      end
    end
  end
  
  -- 活动设置控件
  xlx = 7
  self['活动设置'.."选"..xlx]:置可见(self.类型==xlx)
  self.确定操作:置可见(self.类型==xlx)
  self['选择项目'.."输"..xlx]:置可见(self.类型==xlx)
  
  for i=1,20 do
    if self['物品'..i.."输"..xlx] then
      self['物品'..i.."输"..xlx]:置可见(self.类型==xlx)
    end
  end
  
  for i=1,#xsjl[7] do
    if xsjl[7][i] and self[xsjl[7][i].."输"..xlx] then
      self[xsjl[7][i].."输"..xlx]:置可见(self.类型==xlx)
    end
  end
  
  -- 绘制界面
  self:绘制界面()
end

