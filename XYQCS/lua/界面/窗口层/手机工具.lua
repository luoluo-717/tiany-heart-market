function 手机工具:更新爆率(内容)
    self.bl数据=内容 or {}
    通用列表:清空()
    if type(内容) == 'table' then
        local temp = {}
        for k, v in pairs(内容) do
            if type(k) == 'string' and type(v) == 'table' then
                local order = v.排序 or 999   -- 默认值999，这样没有排序字段的活动会排在后面
                table.insert(temp, {order, k})
            end
        end
        table.sort(temp, function(a, b) 
            local aOrder = tonumber(a[1]) or 999
            local bOrder = tonumber(b[1]) or 999
            return aOrder < bOrder
        end)
        for _, item in ipairs(temp) do
            通用列表:添加(item[2])   -- item[2]是活动名称
        end
    end
    self.类型=7
    self:重刷()
end

