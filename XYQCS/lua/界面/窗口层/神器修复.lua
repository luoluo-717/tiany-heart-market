function 神器修复:五行重置()
    if self.神器属性.神器解锁[self.选中]~=nil then
        神器修复.开始:置可见(false)
    else
        神器修复.开始:置可见(true)
    end
    神器修复.放弃:置可见(false)
    神器修复.保存:置可见(false)
    for n=1,3 do
        -- if self.神器属性.神器解锁[n]==nil then
        --     神器修复["神器"..n]:置禁止(true)
        -- else
        if self.选中 == n then
            神器修复["神器"..n]:置禁止(false)
            神器修复["神器"..n]:置选中(true)
        else
            神器修复["神器"..n]:置禁止(false)
        end
    end
    for n=1,3 do
        if self.神器属性.神器解锁[n]~=nil then
            if self.选中 == nil then 
                self.选中 = n 
                break 
            end
        end
    end
    self.五行控件:重置()
end

