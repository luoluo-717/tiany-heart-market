    self.排行数据 = self.数据 and self.数据[排行类型[i]] or {}  -- 直接存储当前排行数据
    self.模型动画列表 = {}  -- 清空模型

    -- 加载前三名模型（保留原逻辑）
    for 排名 = 1, math.min(3, #self.排行数据) do
        local 排行项 = self.排行数据[排名]
        self.模型动画列表[排名] = 加载排行模型(排行项)
    end
end

