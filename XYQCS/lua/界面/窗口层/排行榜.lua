-- 模型加载函数（完全保留）
local function 加载排行模型(排行数据项)
    local 模型动画 = {
        角色 = nil,
        武器 = nil,
        副武器 = nil,
        光环 = nil
    }
    local 允许武器 = true
    local 锦衣名称 = nil

    if 排行数据项.锦衣 then
        锦衣名称 = 排行数据项.锦衣.名称
        for _, 名称 in ipairs(隐藏武器的锦衣) do
            if 锦衣名称 == 名称 then
                允许武器 = false
                break
            end
        end
        if 允许武器 then
            local 武器类型 = 排行数据项.武器 and 多开数据[当前].__主控.取武器子类(排行数据项.武器.子类) or nil
            local lssj = 取模型(排行数据项.模型, nil, nil, 锦衣名称, 武器类型)
            模型动画.角色 = __res:取动画2(__res:取地址('shape/mx/zj/', lssj[1])):置方向(5):置循环(true)
        else
            local lssj = 取祥瑞模型(排行数据项.模型, 锦衣名称)
            模型动画.角色 = __res:取动画2(__res:取地址('shape/mx/zj/', lssj[1])):置方向(5):置循环(true)
        end
    else
        local lssj = 取模型(排行数据项.模型)
        模型动画.角色 = __res:取动画2(__res:取地址('shape/mx/', lssj[1])):置方向(5):置循环(true)
    end

    if 允许武器 and 排行数据项.武器 then
        local 武器附加名 = 多开数据[当前].__主控:取武器附加名称(
            排行数据项.武器.子类,
            排行数据项.武器.等级,
            排行数据项.武器.名称
        )
        local lssj = 取模型(武器附加名 .. "_" .. 排行数据项.模型)
        模型动画.武器 = __res:取动画2(__res:取地址('shape/mx/', lssj[1])):置方向(5):置循环(true)
    end

    if 允许武器 and 排行数据项.副武器 and 排行数据项.模型 == "影精灵" then
        local 副武器附加名 = 多开数据[当前].__主控:取武器附加名称(
            排行数据项.副武器.子类,
            排行数据项.副武器.等级,
            排行数据项.副武器.名称
        )
        local lssj = 取模型(副武器附加名 .. "_" .. 排行数据项.模型)
        模型动画.副武器 = __res:取动画2(__res:取地址('shape/mx/', lssj[1])):置方向(5):置循环(true)
    end

    if 排行数据项.光环 then
        local 光环模型 = 取光环(排行数据项.光环.名称)
        模型动画.光环 = __res:取动画(__res:取地址("shape/mx/", 光环模型[1])):取动画(5):置当前帧(1):播放(true)
    end

    return 模型动画
end

