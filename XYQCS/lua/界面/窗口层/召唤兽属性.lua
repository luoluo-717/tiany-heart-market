local 五行属性 = 召唤兽属性:创建我的was按钮(__res:取地址('shape/ui/',0x6b4f596f), "五行属性", 83, 235)
function 五行属性:左键弹起(x, y, msg)
    -- 检查是否选中召唤兽
    if not 召唤兽属性.选中召唤兽 then
        多开数据[当前].__UI弹出.提示框:打开("#Y/请先选中召唤兽")
        return
    end
    local 选中宝宝 = 多开数据[当前].角色信息.宝宝列表[召唤兽属性.选中召唤兽]
    if not 选中宝宝 then
        多开数据[当前].__UI弹出.提示框:打开("#R/选中的召唤兽数据不存在")
        return
    end
    local 有效词条 = {}
    for _, 词条名 in ipairs(词条属性列表) do
        if 选中宝宝[词条名] ~= nil and 选中宝宝[词条名] ~= 0 then
            table.insert(有效词条, {
                五行 = 词条五行映射[词条名] or "未知",
                数值 = 选中宝宝[词条名]
            })
        end
    end
    local 提示内容 = ""
    if #有效词条 == 0 then
        提示内容 = "#Y/该召唤兽无五行属性词条"
    else
        for i, 词条 in ipairs(有效词条) do
            local 符号 = 词条.数值 > 0 and "+" or ""
            提示内容 = 提示内容 .. "#Y/五行属性：#G" .. 词条.五行 .. 符号 .. 词条.数值.."%"
            if i ~= #有效词条 then
                提示内容 = 提示内容 .. "\n"
            end
        end
    end
    多开数据[当前].__UI弹出.提示框:打开(提示内容)
end

function 召唤兽属性:检查五行按钮可见性()
    if not 召唤兽属性.选中召唤兽 then
        召唤兽属性.五行属性:置可见(false)
        return
    end
    local 选中宝宝 = 多开数据[当前].角色信息.宝宝列表[召唤兽属性.选中召唤兽]
    if not 选中宝宝 then
        召唤兽属性.五行属性:置可见(false)
        return
    end
    local 有有效词条 = false
    for _, 词条名 in ipairs(词条属性列表) do
        if 选中宝宝[词条名] ~= nil and 选中宝宝[词条名] ~= 0 then
            有有效词条 = true
            break  -- 找到一个有效词条即可
        end
    end
    召唤兽属性.五行属性:置可见(有有效词条)
end
